var meksikodugme = document.getElementById("meksikodugme");
var stiklirano = document.getElementById("stiklirano");

meksikodugme.onclick = function(){
    stiklirano.style.visibility = "visible";
    stiklirano.style.opacity = "1";

    setTimeout(function(){
        stiklirano.style.opacity = "0";
    },2000)
}

